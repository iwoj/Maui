package com.bitmovers.maui;


// ======================================================================
// CLASS: MauiException
// ======================================================================

/** The MauiException class is the super class of all Exceptions that are
  * a part of Maui.
  *
  * @author Patrick Gibson (patrick@bitmovers.com)
  *
  */

public class MauiException extends Exception
{
  // --------------------------------------------------------------------
  // CONSTRUCTOR: MauiException
  // --------------------------------------------------------------------

  public MauiException()
  {
    super();
  }


  // --------------------------------------------------------------------
  // CONSTRUCTOR: MauiException
  // --------------------------------------------------------------------

  public MauiException(String string)
  {
    super(string);
  }


  // --------------------------------------------------------------------
}

